    {
        Kelas::create([
            'id_guru' => auth()->user()->id,
            'mata_pelajaran' => $request->mata_pelajaran,
            'kelas' => $request->kelas,
            'jurusan' => $request->jurusan,
            'tentang_pelajaran' => $request->tentang_pelajaran,
        ]);

        return redirect()->back();
    }

